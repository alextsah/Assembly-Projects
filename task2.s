.global _start
.equ TIMERLOAD, 0xFFFEC600
.equ TIMERCOUNTER, 0xFFFEC604
.equ TIMERCONTROL, 0xFFFEC608

_start:
	MOV R1,#0x4
	MOV R2,#0b11111111111111111111111111111111
	BL ARM_TIM_config_ASM
	B end 


ARM_TIM_config_ASM:
	PUSH {R3-R12}
	LDR R3,=TIMERLOAD
	STR R1,[R3]
	LDR R4,=TIMERCONTROL
	STR R2,[R4]
	POP {R3-R12}
	BX LR
	
	
end:
	B end 
	.end
